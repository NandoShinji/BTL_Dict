    private void btAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAddActionPerformed
        ArrayList<Word> nword = dic.word;
        String wt = "", we = "";
        wt = textAdd.getText();
        we = areaAdd.getText();
        String[] w1 = new String[dic.word.size()];
        int i = 0;
        while (i < dic.word.size()) {
            w1[i] = dic.word.get(i).getWordTarget();
            i++;
        }
        int index = Arrays.binarySearch(w1, wt);
        if (wt.equals("") || we.equals("")) {
            JOptionPane.showMessageDialog(null, "Word or Explain is emty!");
        } else {
            if (index > 0) {
                JOptionPane.showMessageDialog(null, "Từ này đã tồn tại!");
            } else {
                Word wors = new Word(wt, we);
                dic.word.add(wors);
                listModel.addElement(wt);

                listWord.setModel(listModel);
                JOptionPane.showMessageDialog(null, "Done!");

            }
        }
    }//GEN-LAST:event_btAddActionPerformed

    private void btRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btRemoveActionPerformed
        if (!textWord.getText().equals("")) {
            int option = JOptionPane.showConfirmDialog(null, "Bạn có chắc chắn không?", "Remove Word", JOptionPane.OK_CANCEL_OPTION);
            if (option == JOptionPane.OK_OPTION) {
                int index = listWord.getSelectedIndex();
                dic.removeWord(textWord.getText());
                listModel.remove(index);
                listWord.setModel(listModel);
                areaExplain.setText("");
                textWord.setText("");
                JOptionPane.showMessageDialog(null, "Done!");
            }

        }
    }//GEN-LAST:event_btRemoveActionPerformed

