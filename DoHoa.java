    private void btEVActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btEVActionPerformed
        String we = "";
        Translate translate = new Translate();
        String s = textWord.getText();

        String regax = "(?<!\\\\)" + Pattern.quote(".");
        String[] arr = s.split(regax);
        ArrayList<String> str = new ArrayList<>();
        for (String w : arr) {
            str.add(w + ". ");
        }
        for (String w : str) {
            try {
                we += translate.callTranslate("en", "vi", w);

            } catch (Exception ex) {
                Logger.getLogger(DoHoa.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        areaExplain.setText(we);


    }//GEN-LAST:event_btEVActionPerformed

