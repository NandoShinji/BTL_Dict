    private void textWordActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_textWordActionPerformed
        if (!textWord.getText().equals("")) {
            String[] w = new String[dic.word.size()];
            int i = 0;
            while (i < dic.word.size()) {
                w[i] = dic.word.get(i).getWordTarget();
                i++;
            }
            int index = Arrays.binarySearch(w, textWord.getText());
            if (index < 0) {
                JOptionPane.showMessageDialog(null, "Word Cannot Found!");
            } else {
                areaExplain.setText(dic.word.get(index).getWordExplain());
            }
        }
    }//GEN-LAST:event_textWordActionPerformed

